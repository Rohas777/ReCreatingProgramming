int great_com_div(int a, int b) {
    return fabs(b ? great_com_div(b, a % b) : a);
}

class Rational {
private:
    int m, n;
    double res;
public:
    Rational() {
        this->m = 0; this->n = 0;
        this->res = this->m / (double)n;
    }
    Rational(int a, int b) {
        this->m = a; this->n = b;
        this->res = this->m / (double)n;
    }

    static int gcd(int a, int b) {
        return great_com_div(a, b);
    }
    bool operator ==(Rational right) {
        if (this->res == right.res) return true;
        else return false;
    }
    Rational operator +(Rational right) {
        Rational temp;
        if (right.isNaN()) temp.res = this->res + right.isNaN();
        else {
            temp.res = this->res + right.res;
           // temp.n = this->n + right.n;
           // temp.m = right.n * this->m + this->n * right.m;
        }
        return temp;
    }
    Rational operator -(Rational right) {
        Rational temp;
        if (right.isNaN()) temp.res = this->res - right.isNaN();
        else {
            temp.res = this->res - right.res;
            // temp.n = this->n + right.n;
            // temp.m = right.n * this->m - this->n * right.m;
        }
        return temp;
    }
    Rational operator *(Rational right) {
        Rational temp;
        if (right.isNaN()) temp.res = this->res * right.isNaN();
        else {
            temp.res = this->res * right.res;
        }
        return temp;
    }
    Rational operator /(Rational right) {
        Rational temp;
        temp.m = this->m * right.n;
        temp.n = this->n * right.m;
        return temp;
    }
    operator double() {
        double temp;
        temp = this->m / this->n;
        return temp;
    }
    operator bool() {
        if (this->m / this->n == 0) return false;
        else return true;
    }
    int numerator() {
        return this->m;
    }
    int denominator() {
        return this->n;
    }
    bool isNaN() {
        if (this->m == 0 && this->n == 0) return true;
        else return false;
    }
};

